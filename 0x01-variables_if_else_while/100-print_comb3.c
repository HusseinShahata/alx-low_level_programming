#include <stdio.h>

/*
 * main - Write a program that prints all possible different combinations of two digits
 * 	  Numbers must be separated by , followed by a space
 * 	  The two digits must be different.
 *
 * Return : 0 success
 */

int main(void)
{
	int c = '01';

	while (c <= 98)
	{
		putchar('c');
		putchar(',');
		putchar(' ');
	}
	c++;

	putchar('\n');

	return (0);
}
